#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 3;
    cout << "Sum: " << a + b << endl;           
	// Outputs: Sum: 13
    cout << "Difference: " << a - b << endl;   
	 // Outputs: Difference: 7
    cout << "Product: " << a * b << endl;       
	// Outputs: Product: 30
    cout << "Quotient: " << a / b << endl;    
	  // Outputs: Quotient: 3
    cout << "Remainder: " << a % b << endl;    
	 // Outputs: Remainder: 1
    return 0;
}

